def add(x: int, y: int) -> int:
    """
    add two number together
    :param x: int
    :param y: int
    :return: int
    """
    return x + y


def subtract(x: int, y: int) -> int:
    """
    subtract x from y and return value
    :param x: int
    :param y: int
    :return: int
    """
    return y - x
